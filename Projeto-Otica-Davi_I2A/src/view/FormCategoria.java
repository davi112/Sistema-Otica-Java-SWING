    public void atualizaLista() {
        
        listaCategorias.clear();
        listaCategorias.addAll(dao.getLista());
        int linha = listaCategorias.size() - 1;
        
        if (linha >= 0) {
            listaCategoriasVisual.setSelectedIndex(linha);
        }
    }

    public void setIcon() {
        setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/images/oculos.png")));
    }

    public void trataEdicao(boolean editando) {
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        btnNovo.setEnabled(!editando);
        btnExcluir.setEnabled(!editando);
        txtNome.setEditable(editando);
        listaCategoriasVisual.setEnabled(!editando);
    }

    public void limpaCamposCasoNaoHajaCadastros() {
        if (this.listaCategoriasVisual.getModel().getSize() == 0) {
            txtIdCategoria.setText("");
            txtNome.setText("");
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
    }

    public boolean validaCampos() {
        StringBuilder mensagem = new StringBuilder();
        if (txtNome.getText().length() <= 0) {
            mensagem.append("Informe o nome da categoria!");
            txtNome.requestFocus();
        }
        if (mensagem.toString().length() != 0) {
             JOptionPane.showMessageDialog(this, mensagem.toString(), "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }


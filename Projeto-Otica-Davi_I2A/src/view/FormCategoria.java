    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            int linhaSelecionada = listaCategoriasVisual.getSelectedIndex();
            Categoria obj = listaCategorias.get(linhaSelecionada);
            dao.salvar(obj);
            trataEdicao(false);
            atualizaLista();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here
        int option = JOptionPane.showOptionDialog(this, "Deseja realmente excluir essa categoria ?",
                "Caixa de confirmação",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, new String[]{"Sim", "Não"}, "Sim");
        if (option == 0) {

            int linhaSelecionada = listaCategoriasVisual.getSelectedIndex();
            Categoria categoria = listaCategorias.get(linhaSelecionada);
            dao.remover(categoria);
            atualizaLista();
        }
        limpaCamposCasoNaoHajaCadastros();
    }//GEN-LAST:event_btnExcluirActionPerformed

